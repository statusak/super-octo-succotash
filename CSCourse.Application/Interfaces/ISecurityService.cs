using CSCourse.Domain.Models;
using CSCourse.Application.Models;

namespace CSCourse.Application.Interfaces;

/// <summary>
/// Сервис безопасности, предоставляющий низкоуровневые криптографические операции:
/// хеширование паролей для безопасного хранения, проверку паролей при аутентификации
/// и генерацию JWT-токенов для авторизации запросов. Реализуется в слое Infrastructure,
/// так как зависит от внешних библиотек и конфигураций (например, секретного ключа для подписи токенов).
/// </summary>
public interface ISecurityService
{
    /// <summary>
    /// Хеширует переданный пароль с использованием надёжного алгоритма (например, bcrypt, PBKDF2 или Argon2).
    /// Результат может быть безопасно сохранён в базе данных вместо открытого пароля.
    /// </summary>
    /// <param name="password">Пароль в открытом виде, который необходимо захешировать.</param>
    /// <returns>Строка, содержащая хеш пароля, пригодная для хранения и последующей проверки.</returns>
    string HashPassword(string password);

    /// <summary>
    /// Проверяет соответствие переданного пароля сохранённому хешу. Используется при аутентификации пользователя.
    /// </summary>
    /// <param name="password">Пароль, введённый пользователем (в открытом виде).</param>
    /// <param name="hash">Сохранённый хеш пароля, с которым необходимо сравнить введённое значение.</param>
    /// <returns><see langword="true"/>, если пароль соответствует хешу; в противном случае — <see langword="false"/>.</returns>
    bool VerifyPassword(string password, string hash);

    /// <summary>
    /// Генерирует JWT-токен на основе переданной информации о пользователе: его идентификатора, логина и роли.
    /// Токен подписывается секретным ключом и содержит утверждения (claims), позволяющие идентифицировать пользователя
    /// и проверить его права доступа на стороне API.
    /// </summary>
    /// <param name="accountJWTInfoDto">Объект <see cref="AccountJWTInfoDto"/>, содержащий сведения о пользователе:
    /// идентификатор, логин и роль, которые будут закодированы в JWT-токене.</param>
    /// <returns>Строковое представление JWT-токена, которое может быть передано клиенту для использования
    /// в заголовке Authorization последующих запросов.</returns>
    string CreateJwtToken(AccountJWTInfoDto accountJWTInfoDto);
}
