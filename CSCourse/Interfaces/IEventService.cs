using CSCourse.Models;
using System.ComponentModel.DataAnnotations;

namespace CSCourse.Interfaces
{
    /// <summary>
    /// Сервис для работы с мероприятиями (Events). Предоставляет бизнес‑логику для управления событиями:
    /// получение списка с фильтрацией и пагинацией, поиск по ID, создание, обновление, удаление,
    /// а также управление доступностью мест (бронирование и освобождение).
    /// </summary>
    public interface IEventService
    {
        /// <summary>
        /// Получает пагинированный список всех мероприятий без фильтрации.
        /// </summary>
        /// <param name="page">Номер запрашиваемой страницы (нумерация с 1).</param>
        /// <param name="pageSize">Количество элементов на странице.</param>
        /// <returns>Объект <see cref="PaginatedResult"/>, содержащий:
        /// - список мероприятий (<c>items</c>);
        /// - общее количество записей (<c>totalCount</c>);
        /// - номер текущей страницы (<c>page</c>);
        /// - размер страницы (<c>pageSize</c>).</returns>
        /// <remarks>
        /// Метод возвращает базовые данные о мероприятиях, достаточные для отображения в каталоге.
        /// Для получения полной информации по конкретному мероприятию используйте <see cref="GetEventById(Guid)"/>.
        /// </remarks>
        PaginatedResult GetAll(int page, int pageSize);

        /// <summary>
        /// Получает пагинированный список всех мероприятий без фильтрации (асинхронно).
        /// </summary>
        /// <param name="page">Номер запрашиваемой страницы (нумерация с 1).</param>
        /// <param name="pageSize">Количество элементов на странице.</param>
        /// <returns>Объект <see cref="PaginatedResult"/>, содержащий:
        /// - список мероприятий (<c>items</c>);
        /// - общее количество записей (<c>totalCount</c>);
        /// - номер текущей страницы (<c>page</c>);
        /// - размер страницы (<c>pageSize</c>).</returns>
        /// <remarks>
        /// Метод возвращает базовые данные о мероприятиях, достаточные для отображения в каталоге.
        /// Для получения полной информации по конкретному мероприятию используйте <see cref="GetEventById(Guid)"/>.
        /// </remarks>
        Task<PaginatedResult> GetAllAsync(int page, int pageSize);

        /// <summary>
        /// Получает пагинированный список мероприятий с применением фильтров.
        /// </summary>
        /// <param name="filterEvent">Объект фильтра с критериями отбора мероприятий.</param>
        /// <param name="page">Номер запрашиваемой страницы (нумерация с 1).</param>
        /// <param name="pageSize">Количество элементов на странице.</param>
        /// <returns>Объект <see cref="PaginatedResult"/> с отфильтрованным и пагинированным списком мероприятий.</returns>
        /// <remarks>
        /// Поддерживаемые критерии фильтрации:
        /// - частичное совпадение по названию (без учёта регистра);
        /// - диапазон дат начала (<c>startAt</c>);
        /// - диапазон дат окончания (<c>endAt</c>).
        /// Фильтрация выполняется на стороне сервера, что позволяет эффективно работать с большими объёмами данных.
        /// </remarks>
        PaginatedResult GetAll(FilterEvent filterEvent, int page, int pageSize);

        /// <summary>
        /// Получает пагинированный список мероприятий с применением фильтров (асинхронно).
        /// </summary>
        /// <param name="filterEvent">Объект фильтра с критериями отбора мероприятий.</param>
        /// <param name="page">Номер запрашиваемой страницы (нумерация с 1).</param>
        /// <param name="pageSize">Количество элементов на странице.</param>
        /// <returns>Объект <see cref="PaginatedResult"/> с отфильтрованным и пагинированным списком мероприятий.</returns>
        /// <remarks>
        /// Поддерживаемые критерии фильтрации:
        /// - частичное совпадение по названию (без учёта регистра);
        /// - диапазон дат начала (<c>startAt</c>);
        /// - диапазон дат окончания (<c>endAt</c>).
        /// Фильтрация выполняется на стороне сервера, что позволяет эффективно работать с большими объёмами данных.
        /// </remarks>
        Task<PaginatedResult> GetAllAsync(FilterEvent filterEvent, int page, int pageSize);

        /// <summary>
        /// Получает детальную информацию о мероприятии по его уникальному идентификатору.
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия.</param>
        /// <returns>Объект <see cref="Event"/>, если мероприятие найдено; <c>null</c> — если мероприятие с указанным ID отсутствует.</returns>
        /// <remarks>
        /// Возвращает полную информацию о мероприятии, включая все поля модели <see cref="Event"/>.
        /// Рекомендуется использовать только для детального просмотра конкретного мероприятия.
        /// </remarks>
        Event? GetEventById(Guid id);

        /// <summary>
        /// Получает детальную информацию о мероприятии по его уникальному идентификатору (асинхронно).
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия.</param>
        /// <returns>Объект <see cref="Event"/>, если мероприятие найдено; <c>null</c> — если мероприятие с указанным ID отсутствует.</returns>
        /// <remarks>
        /// Возвращает полную информацию о мероприятии, включая все поля модели <see cref="Event"/>.
        /// Рекомендуется использовать только для детального просмотра конкретного мероприятия.
        /// </remarks>
        Task<Event?> GetEventByIdAsync(Guid id);

        /// <summary>
        /// Пытается зарезервировать указанное количество мест на мероприятии.
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, для которого резервируются места.</param>
        /// <param name="count">Количество мест для резервирования (по умолчанию — 1).</param>
        /// <returns><c>true</c>, если места успешно зарезервированы; <c>false</c>, если недостаточно свободных мест.</returns>
        /// <remarks>
        /// Операция выполняется атомарно (с блокировкой) для предотвращения гонок данных.
        /// Уменьшает значение <see cref="Event.AvailableSeats"/> на указанное количество, если это возможно.
        /// </remarks>
        bool TryReserveSeats(Guid id, int count = 1);

        /// <summary>
        /// Пытается зарезервировать указанное количество мест на мероприятии (асинхронно).
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, для которого резервируются места.</param>
        /// <param name="count">Количество мест для резервирования (по умолчанию — 1).</param>
        /// <returns><c>true</c>, если места успешно зарезервированы; <c>false</c>, если недостаточно свободных мест.</returns>
        /// <remarks>
        /// Операция выполняется атомарно (с блокировкой) для предотвращения гонок данных.
        /// Уменьшает значение <see cref="Event.AvailableSeats"/> на указанное количество, если это возможно.
        /// </remarks>
        Task<bool> TryReserveSeatsAsync(Guid id, int count = 1);

        /// <summary>
        /// Пытается освободить указанное количество ранее зарезервированных мест на мероприятии.
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, для которого освобождаются места.</param>
        /// <param name="count">Количество освобождаемых мест (по умолчанию — 1).</param>
        /// <returns><c>true</c>, если места успешно освобождены; <c>false</c>, если общее количество мест будет превышено.</returns>
        /// <remarks>
        /// Операция выполняется атомарно (с блокировкой) для предотвращения гонок данных.
        /// Увеличивает значение <see cref="Event.AvailableSeats"/> на указанное количество, если это не приведёт к превышению <see cref="Event.TotalSeats"/>.
        /// </remarks>
        bool ReleaseSeats(Guid id, int count = 1);

        /// <summary>
        /// Пытается освободить указанное количество ранее зарезервированных мест на мероприятии (асинхронно).
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, для которого освобождаются места.</param>
        /// <param name="count">Количество освобождаемых мест (по умолчанию — 1).</param>
        /// <returns><c>true</c>, если места успешно освобождены; <c>false</c>, если общее количество мест будет превышено.</returns>
        /// <remarks>
        /// Операция выполняется атомарно (с блокировкой) для предотвращения гонок данных.
        /// Увеличивает значение <see cref="Event.AvailableSeats"/> на указанное количество, если это не приведёт к превышению <see cref="Event.TotalSeats"/>.
        /// </remarks>
        Task<bool> ReleaseSeatsAsync(Guid id, int count = 1);

        /// <summary>
        /// Проверяет, существует ли мероприятие с указанным уникальным идентификатором.
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия.</param>
        /// <returns><c>true</c>, если мероприятие с указанным ID существует; <c>false</c> — если мероприятие не найдено.</returns>
        /// <remarks>
        /// Метод выполняет проверку наличия мероприятия в хранилище без получения детальной информации.
        /// В отличие от методов, возвращающих объект <see cref="Event"/>, данный метод оптимизирован для быстрых проверок существования.
        /// </remarks>
        bool IsEventExists(Guid id);

        /// <summary>
        /// Проверяет, существует ли мероприятие с указанным уникальным идентификатором (асинхронно).
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия.</param>
        /// <returns><c>true</c>, если мероприятие с указанным ID существует; <c>false</c> — если мероприятие не найдено.</returns>
        /// <remarks>
        /// Метод выполняет проверку наличия мероприятия в хранилище без получения детальной информации.
        /// В отличие от методов, возвращающих объект <see cref="Event"/>, данный метод оптимизирован для быстрых проверок существования.
        /// </remarks>
        Task<bool> IsEventExistsAsync(Guid id);

        /// <summary>
        /// Создаёт новое мероприятие в системе.
        /// </summary>
        /// <param name="event">Объект <see cref="Event"/> с данными для создания мероприятия.</param>
        /// <returns>Уникальный идентификатор (<see cref="Guid"/>) созданного мероприятия.</returns>
        /// <exception cref="ValidationException">Выбрасывается при нарушении правил валидации данных.</exception>
        /// <exception cref="ArgumentNullException">Выбрасывается, если переданный объект <c>event</c> равен <c>null</c>.</exception>
        /// <remarks>
        /// При создании автоматически инициализирует начальное количество доступных мест (<see cref="Event.AvailableSeats"/>)
        /// равным общему количеству мест (<see cref="Event.TotalSeats"/>).
        /// </remarks>
        Guid CreateEvent(Event @event);

        /// <summary>
        /// Создаёт новое мероприятие в системе (асинхронно).
        /// </summary>
        /// <param name="event">Объект <see cref="Event"/> с данными для создания мероприятия.</param>
        /// <returns>Уникальный идентификатор (<see cref="Guid"/>) созданного мероприятия.</returns>
        /// <exception cref="ValidationException">Выбрасывается при нарушении правил валидации данных.</exception>
        /// <exception cref="ArgumentNullException">Выбрасывается, если переданный объект <c>event</c> равен <c>null</c>.</exception>
        /// <remarks>
        /// При создании автоматически инициализирует начальное количество доступных мест (<see cref="Event.AvailableSeats"/>)
        /// равным общему количеству мест (<see cref="Event.TotalSeats"/>).
        /// </remarks>
        Task<Guid> CreateEventAsync(Event @event);

        /// <summary>
        /// Полностью обновляет данные существующего мероприятия.
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, которое необходимо обновить.</param>
        /// <param name="event">Объект <see cref="Event"/> с обновлёнными данными мероприятия.
        /// ID в объекте должен соответствовать параметру <c>id</c>.</param>
        /// <exception cref="InvalidOperationException">Выбрасывается, если мероприятие с указанным ID не найдено.</exception>
        /// <exception cref="ValidationException">Выбрасывается при нарушении правил валидации данных.</exception>
        /// <exception cref="ArgumentNullException">Выбрасывается, если переданный объект <c>event</c> равен <c>null</c>.</exception>
        bool UpdateEvent(Guid id, Event @event);

        /// <summary>
        /// Полностью обновляет данные существующего мероприятия (асинхронно).
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, которое необходимо обновить.</param>
        /// <param name="event">Объект <see cref="Event"/> с обновлёнными данными мероприятия.
        /// ID в объекте должен соответствовать параметру <c>id</c>.</param>
        /// <exception cref="InvalidOperationException">Выбрасывается, если мероприятие с указанным ID не найдено.</exception>
        /// <exception cref="ValidationException">Выбрасывается при нарушении правил валидации данных.</exception>
        /// <exception cref="ArgumentNullException">Выбрасывается, если переданный объект <c>event</c> равен <c>null</c>.</exception>
        Task<bool> UpdateEventAsync(Guid id, Event @event);

        /// <summary>
        /// Частично обновляет данные существующего мероприятия, позволяя изменить ключевые поля.
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, которое необходимо обновить.</param>
        /// <param name="Title">Новое название мероприятия.</param>
        /// <param name="Description">Новое описание мероприятия (может быть <c>null</c>).</param>
        /// <param name="StartAt">Новая дата и время начала мероприятия.</param>
        /// <param name="EndAt">Новая дата и время окончания мероприятия.</param>
        /// <exception cref="InvalidOperationException">Выбрасывается, если мероприятие с указанным ID не найдено.</exception>
        /// <remarks>
        /// Альтернативный метод обновления, удобный для сценариев, где не требуется передавать полный объект <see cref="Event"/>.
        /// Позволяет гибко обновлять только необходимые поля.
        /// </remarks>
        bool UpdateEvent(Guid id, string Title, string? Description, DateTime StartAt, DateTime EndAt);

        /// <summary>
        /// Частично обновляет данные существующего мероприятия, позволяя изменить ключевые поля (асинхронно).
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, которое необходимо обновить.</param>
        /// <param name="Title">Новое название мероприятия.</param>
        /// <param name="Description">Новое описание мероприятия (может быть <c>null</c>).</param>
        /// <param name="StartAt">Новая дата и время начала мероприятия.</param>
        /// <param name="EndAt">Новая дата и время окончания мероприятия.</param>
        /// <exception cref="InvalidOperationException">Выбрасывается, если мероприятие с указанным ID не найдено.</exception>
        /// <remarks>
        /// Альтернативный метод обновления, удобный для сценариев, где не требуется передавать полный объект <see cref="Event"/>.
        /// Позволяет гибко обновлять только необходимые поля.
        /// </remarks>
        Task<bool> UpdateEventAsync(Guid id, string Title, string? Description, DateTime StartAt, DateTime EndAt);

        /// <summary>
        /// Удаляет мероприятие из системы по его уникальному идентификатору.
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, подлежащего удалению.</param>
        /// <exception cref="InvalidOperationException">Выбрасывается, если мероприятие с указанным ID не найдено.</exception>
        /// <remarks>
        /// Операция необратима. Все связанные данные (например, регистрации участников) также могут быть удалены
        /// в соответствии с бизнес‑правилами системы. Рекомендуется предварительно проверять существование мероприятия
        /// с помощью метода <see cref="IsEventExists(Guid)"/>.
        /// </remarks>
        bool DeleteEvent(Guid id);

        /// <summary>
        /// Удаляет мероприятие из системы по его уникальному идентификатору (асинхронно).
        /// </summary>
        /// <param name="id">Уникальный идентификатор мероприятия, подлежащего удалению.</param>
        /// <exception cref="InvalidOperationException">Выбрасывается, если мероприятие с указанным ID не найдено.</exception>
        /// <remarks>
        /// Операция необратима. Все связанные данные (например, регистрации участников) также могут быть удалены
        /// в соответствии с бизнес‑правилами системы. Рекомендуется предварительно проверять существование мероприятия
        /// с помощью метода <see cref="IsEventExists(Guid)"/>.
        /// </remarks>
        Task<bool> DeleteEventAsync(Guid id);
    }
}
