namespace CSCourse.Models
{
    /// <summary>
    /// Модель для фильтрации мероприятий на уровне бизнес‑логики и доступа к данным.
    /// Преобразуется из FilterEventDto после валидации.
    /// </summary>
    /// <remarks>
    /// Отличается от FilterEventDto тем, что поле Title является обязательным (required)
    /// и обычно уже нормализовано (например, приведено к нижнему регистру для поиска).
    /// </remarks>
    public class FilterEvent
    {
        /// <summary>
        /// Нормализованное название мероприятия для поиска по частичному совпадению.
        /// </summary>
        /// <remarks>
        /// Значение всегда задано (не null), даже если в исходном запросе оно отсутствовало
        /// (в этом случае устанавливается пустая строка "").
        /// Обычно приводится к нижнему регистру для упрощения поиска без учёта регистра.
        /// </remarks>
        public required string Title { get; set; }

        /// <summary>
        /// Нижняя граница диапазона дат начала мероприятий.
        /// </summary>
        /// <remarks>
        /// Фильтрует мероприятия, которые начинаются в указанную дату или позже.
        /// Если null, ограничение не применяется.
        /// </remarks>
        public DateTime? StartAt { get; set; }

        /// <summary>
        /// Верхняя граница диапазона дат окончания мероприятий.
        /// </summary>
        /// <remarks>
        /// Фильтрует мероприятия, которые заканчиваются в указанную дату или раньше.
        /// Если null, ограничение не применяется.
        /// Должна быть не раньше, чем StartAt (проверка выполняется на более высоком уровне).
        /// </remarks>
        public DateTime? EndAt { get; set; }
    }
}
