namespace CSCourse.Models
{
    /// <summary>
    /// Класс, содержащий информацию о мероприятии.
    /// </summary>
    public class Event
    {
        /// <summary>
        /// Уникальный идентификатор мероприятия.
        /// Генерируется автоматически при создании записи, используется для однозначной идентификации события в системе.
        /// </summary>
        public required Guid Id { get; set; }

        /// <summary>
        /// Название мероприятия.
        /// Отображается в списках и карточках событий, должно быть кратким и информативным (например, «Лекция по алгоритмам», «Вебинар: C# для начинающих»).
        /// </summary>
        public required string Title { get; set; }

        /// <summary>
        /// Описание мероприятия (может быть пустым).
        /// Подробная информация о содержании события: темы, формат, особенности. Допустимо отсутствие значения.
        /// </summary>
        public string? Description { get; set; }

        /// <summary>
        /// Общее количество мест на мероприятии.
        /// Определяет максимальную вместимость события (например, вместимость аудитории или лимит регистраций для онлайн-формата).
        /// </summary>
        public required int TotalSeats { get; set; }

        /// <summary>
        /// Количество свободных мест на мероприятии.
        /// Динамическое значение, которое уменьшается при регистрации участников и увеличивается при отмене регистрации.
        /// </summary>
        public required int AvailableSeats { get; set; }

        /// <summary>
        /// Дата и время начала мероприятия.
        /// Указывает момент, когда событие стартует (в формате UTC или локального времени в зависимости от настроек системы).
        /// </summary>
        public required DateTime StartAt { get; set; }

        /// <summary>
        /// Дата и время окончания мероприятия.
        /// Фиксирует момент завершения события; должна быть позже StartAt, валидация выполняется на уровне бизнес-логики.
        /// </summary>
        public required DateTime EndAt { get; set; }
    }
}
